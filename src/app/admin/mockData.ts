// Helper: Push a new Audit Log entry
export function pushAuditLog(
  event: string,
  category: AuditLogEntry["eventCategory"],
  actor: string,
  actorType: AuditLogEntry["actorType"],
  organization: string | null,
  result: "Active" | "Inactive",
  metadata: Record<string, string>,
) {
  const current = db.auditLogs.get();
  const newLog: AuditLogEntry = {
    id: `log-${Date.now()}`,
    event,
    eventCategory: category,
    actor,
    actorType,
    organization,
    ipAddress: "198.51.100.12",
    userAgent: "Google Chrome/124.0.0 (Admin Session)",
    result,
    metadata,
    timestamp: new Date().toISOString(),
  };
  db.auditLogs.save([newLog, ...current]);
}
